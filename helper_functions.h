inline bool is_white(unsigned int piece) {
    return piece >= 0 && piece <= 5;
}

inline bool is_black(unsigned int piece) {
    return piece > 5 && piece < 12;
}

inline bool is_piece(unsigned piece) {
    return piece >= 0 && piece < 12; 
}

